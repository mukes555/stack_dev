if(!function_exists("IG_ANALYTICS_DATA_TYPE")){
    function IG_ANALYTICS_DATA_TYPE($result, $type = ""){  
        $columns = "";
    	$stats = "";
        foreach ($result as $key => $value) {
            switch ($type) {
                case 'country':
                    $columns .= "'".IG_COUNTRY($key)."',";
                    break;
                
                default:
                    $columns .= "'".$key."',";
                    break;
            }
            
            $stats .= $value.",";
        }

        return [
            "columns" => "[".substr($columns, 0, -1)."]",
            "stats" => "[".substr($stats, 0, -1)."]"
        ];
    }
}

if(!function_exists("IG_ANALYTICS_DATA_MAP")){
    function IG_ANALYTICS_DATA_MAP($result, $type = ""){  
        $stats = "";
        foreach ($result as $key => $value) {
            $stats.="{'code':'".$key."','value':".$value."},";
        }
        return "[".substr($stats, 0, -1)."]";
    }
}

if(!function_exists("IG_ANALYTICS_BY_HOUR")){
    function IG_ANALYTICS_BY_HOUR($result){  
    	$columns = "";
    	$stats = "";
    	$data = [];
    	for ($i=0; $i < 24; $i++) { 
    		if($i < 10){ $i = "0".$i; };
    		$columns .= "'".$i."',";
    		if(isset($result[$i])){
    			$stats .= $result[$i].",";
    			$data[$i] = $result[$i];
    		}else{
    			$stats .= "0,";
    			$data[$i] = 0;
    		}
    	}

    	return [
            "columns" => "[".substr($columns, 0, -1)."]",
            "stats" => "[".substr($stats, 0, -1)."]",
            "data" => $data
        ];
    }
}

